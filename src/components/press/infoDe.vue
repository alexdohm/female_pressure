  <h3>sichtbarkeit</h3>

  <p>
    female:pressure ist eine transnationale Online-Datenbank für weibliche,
    transgender und nicht-binäre DJs, Musikerinnen*, Komponistinnen*,
    Produzentinnen*, bildende Künstlerinnen*, Journalistinnen*, Forscherinnen*
    und Vermittlerinnen* aus den Bereichen elektronische Musik und digitale
    Kunst. Sie kann nach verschiedenen, auch verknüpften Kriterien durchsucht
    werden und ist in erster Linie ein Werkzeug, um die Existenz und das
    Schaffen der Künstlerinnen* in dieser scheinbar männerdominierten Szene zu
    verdeutlichen. 2019 feierte female:pressure 21-jähriges Jubiläum.
  </p>

  <p>
    female:pressure dient als öffentlich zugängliche Informationsquelle, die
    1998 von der Wienerin Electric Indigo als technisch systematisierte Antwort
    auf die gängigen Kommentare, dass es so wenig Frauen in der Szene gäbe,
    entworfen und gegründet und von der Open-Source-Expertin Andrea Mayr
    programmiert wurde. Sie umfasst nun über 2700 Künstlerinnen* aus 81 Ländern
    [April 2021], von denen die meisten zu ihrer Homepage verlinkt und viele
    auch per E-Mail erreichbar sind. Zusätzliche Angaben beziehen sich auf den
    Wohnort, die Art der Tätigkeit und den Stil.
  </p>

  <p>
    Die Erfahrung zeigt, dass Frauen* nicht untätiger sind, sondern ihre
    Aktivitäten weniger wahrgenommen oder einfach auch vergessen werden. Das
    Projekt female:pressure wächst und entwickelt sich kontinuierlich weiter.
    Anfangs als reiner Einblick in den Status Quo geplant, hat die Datenbank
    nach mehreren Jahrzehnten auch Archivfunktion. Der einfache Fokus auf Frauen
    hat sich über die Jahre zugunsten einer expliziten Einbindung von trans und
    nicht-binären Geschlechtern erweitert. Eine E-Mail an
    infoATfemalepressure.net mit Informationen über eine noch nicht vertretene
    Künstlerin* und die Rückmeldung der Künstlerin* mit den notwendigen Angaben
    genügen, um in der Datenbank gelistet zu werden.
  </p>

  <h3>vision</h3>

  <p>
    female:pressure betont die Nützlichkeit und Notwendigkeit der Vernetzung und
    Kommunikation in der Szene und dient als Plattform zur Repräsentation nach
    "außen". Hier wurde ein Standard etabliert, auf den verwiesen werden kann
    und den alle Interessierten jederzeit als Informationsquelle nutzen können.
    Die Datenbank ist nach Regionen, Ländern, Stilen, Berufen und Namen
    durchsuchbar. So ist es möglich, sich über bestimmte Künstlerinnen* zu
    informieren oder einfach mal zu schauen, wer wo was macht. Alle auf
    female:pressure gelisteten Members können sich einloggen und ihren
    individuellen Datensatz online aktualisieren. So soll die Wahrscheinlichkeit
    erhöht werden, mit den Informationen auf dem neuesten Stand zu sein.
  </p>

  <p>
    Parallel zum Webprojekt existiert eine interne Mailingliste, auf der etwas
    mehr als die Hälfte der Aktivistinnen* subskribiert ist. Dort werden abseits
    von der allgemein zugänglichen Datenbank Jobs angeboten, neue Projekte
    ausgeheckt, Kontakte gemacht und verstärkt, Hilfestellungen geleistet,
    Erfahrungen diskutiert und Informationen ausgetauscht. Wichtige Resultate
    dieser Mailingliste sind z. B. unsere FACTS Surveys [2013, 2015, 2017 und
    2020], die gemeinsame Presseerklärung #female:pressure, das zweitägige
    Perspectives Festival in Berlin [2013], der VISIBILITY tumblr, unsere
    #Rojava Awareness & Solidarity Aktion [2016] und die female:pressure podcast
    Reihe [seit 2018].
  </p>

